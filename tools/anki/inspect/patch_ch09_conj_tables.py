def process(note_id: str, d: dict):
    path = NOTES_DIR / f"{note_id}.md"
    rows_pres = list(zip(d["pres_ipfv"], d["pres_pfv"]))
    rows_past = list(zip(d["past_ipfv"], d["past_pfv"]))
    table = make_table(
        d["ipfv"], d["pfv"],
        rows_pres, rows_past,
        d["imp_ipfv"], d["imp_pfv"],
    )
    patch_field(path, "Verb_Conj_Table", table)


