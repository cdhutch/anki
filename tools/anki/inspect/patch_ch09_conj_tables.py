def make_table(ipfv_label, pfv_label, rows_pres, rows_past, imp_ipfv, imp_pfv):
    """Build single-line HTML conjugation table."""
    t = (
        f'<table class="conj">'
        f'<tr><th></th><th>IPFV ({ipfv_label})</th><th>PFV ({pfv_label})</th></tr>'
        f'<tr><th colspan="3">Теп./Майб. (Present / Future)</th></tr>'
    )
    pronouns = ["я", "ти", "він/вона", "ми", "ви", "вони"]
    for pronoun, (i, p) in zip(pronouns, rows_pres):
        t += f'<tr><td>{pronoun}</td><td>{i}</td><td>{p}</td></tr>'
    t += '<tr><th colspan="3">Минулий (Past)</th></tr>'
    past_pronouns = ["він", "вона", "вони"]
    for pronoun, (i, p) in zip(past_pronouns, rows_past):
        t += f'<tr><td>{pronoun}</td><td>{i}</td><td>{p}</td></tr>'
    t += '<tr><th colspan="3">Наказовий (Imperative)</th></tr>'
    t += f'<tr><td>2sg / 2pl</td><td>{imp_ipfv[0]} / {imp_ipfv[1]}</td><td>{imp_pfv[0]} / {imp_pfv[1]}</td></tr>'
    t += '</table>'
    return t


def patch_field(path: Path, field: str, value: str):
    text = path.read_text(encoding="utf-8")
    # Match bare empty value or quoted empty string
    pattern = rf"(  {re.escape(field)}: )(''\s*$|$)"
    replacement = f"  {field}: {value}"
    new_text, n = re.subn(pattern, replacement, text, count=1, flags=re.MULTILINE)
    if n == 0:
        raise ValueError(f"Field '{field}' not found in {path}")
    path.write_text(new_text, encoding="utf-8")
    print(f"  Patched {path.name}")


